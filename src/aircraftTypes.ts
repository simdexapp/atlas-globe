/**
 * Resolve an ICAO aircraft type designator to a friendly model name.
 * Falls back to the raw code if no entry is found, so the caller can
 * still display something useful for obscure types.
 */
export function aircraftTypeName(code: string | undefined | null): string {
  if (!code) return "";
  const upper = code.trim().toUpperCase();
  return AIRCRAFT_TYPE_NAMES[upper] || upper;
}
